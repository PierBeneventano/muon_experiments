import os
import sys
import re

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
PROJECT_ROOT = os.path.abspath(os.path.join(SCRIPT_DIR, "..", ".."))
NANOGPT_DIR = os.path.join(PROJECT_ROOT, "nanoGPT")
TRAIN_PY = os.path.join(NANOGPT_DIR, "train.py")

if not os.path.isfile(TRAIN_PY):
    sys.exit(f"ERROR: Cannot find {TRAIN_PY}. Run setup.sh first.")

# ===================================================================
# Step 1: Write muon_utils.py as a standalone module
# ===================================================================

MUON_UTILS = r'''"""
muon_utils.py -- Muon optimizer + spectral logging for nanoGPT.
Auto-generated by patch_muon.py. Do not edit manually.
"""
import torch
import math
import json
import os
import pathlib
import numpy as np


class MuonOptimizer(torch.optim.Optimizer):
    """Muon: momentum + polar decomposition for weight matrices.
    Biases and 1-D params use plain SGD."""

    def __init__(self, params, lr=0.02, momentum=0.95, weight_decay=0.0):
        defaults = dict(lr=lr, momentum=momentum, weight_decay=weight_decay)
        super().__init__(params, defaults)

    @torch.no_grad()
    def step(self, closure=None):
        loss = None
        if closure is not None:
            with torch.enable_grad():
                loss = closure()
        for group in self.param_groups:
            for p in group['params']:
                if p.grad is None:
                    continue
                grad = p.grad.data
                if grad.dim() < 2:
                    p.data.add_(grad, alpha=-group['lr'])
                    continue
                if group['weight_decay'] > 0:
                    p.data.mul_(1.0 - group['lr'] * group['weight_decay'])
                state = self.state[p]
                if 'momentum_buffer' not in state:
                    state['momentum_buffer'] = torch.zeros_like(grad)
                buf = state['momentum_buffer']
                buf.mul_(group['momentum']).add_(grad)
                U, S, Vt = torch.linalg.svd(buf, full_matrices=False)
                p.data.add_(U @ Vt, alpha=-group['lr'])
        return loss


def spectral_entropy(singular_values):
    s = singular_values.clamp(min=1e-12)
    p = s / s.sum()
    return -(p * p.log()).sum().item()


def compute_spectral_metrics(model, full_svd=True):
    records = []
    for name, param in model.named_parameters():
        if param.dim() < 2:
            continue
        with torch.no_grad():
            S = torch.linalg.svdvals(param.data.float())
        h = spectral_entropy(S)
        h_max = math.log(min(param.shape[0], param.shape[1]))
        rec = dict(
            name=name,
            shape=list(param.shape),
            spectral_entropy=h,
            spectral_entropy_normalized=h / h_max if h_max > 0 else 0.0,
            h_max=h_max,
            effective_rank=math.exp(h),
            condition_number=(S[0] / S[-1]).item() if S[-1] > 1e-12 else float('inf'),
            stable_rank=((S**2).sum() / S[0]**2).item(),
            frobenius_norm=(S**2).sum().sqrt().item(),
            nuclear_norm=S.sum().item(),
            operator_norm=S[0].item(),
            top1_sv=S[0].item(),
            sv_mean=S.mean().item(),
            sv_std=S.std().item(),
            numerical_rank=int((S > 1e-6 * S[0]).sum().item()),
        )
        if full_svd:
            rec['singular_values'] = S.cpu().tolist()
        # gradient norms if available
        if param.grad is not None:
            rec['grad_norm'] = param.grad.data.norm().item()
        records.append(rec)
    return records


class SpectralLogger:
    def __init__(self, out_dir, log_every=500, full_svd=True):
        self.out_dir = pathlib.Path(out_dir)
        self.out_dir.mkdir(parents=True, exist_ok=True)
        self.log_every = log_every
        self.full_svd = full_svd
        self.log_path = self.out_dir / "spectral_log.jsonl"

    def maybe_log(self, model, iter_num, extra=None):
        if iter_num % self.log_every != 0:
            return
        metrics = compute_spectral_metrics(model, full_svd=self.full_svd)
        record = dict(iter=iter_num, layers=metrics)
        if extra:
            record.update(extra)
        with open(self.log_path, "a") as f:
            f.write(json.dumps(record) + "\n")
'''

muon_utils_path = os.path.join(NANOGPT_DIR, "muon_utils.py")
with open(muon_utils_path, "w") as f:
    f.write(MUON_UTILS)
print(f"Wrote {muon_utils_path}")

# ===================================================================
# Step 2: Make minimal edits to train.py
# ===================================================================

BACKUP = TRAIN_PY + ".orig"
if not os.path.isfile(BACKUP):
    with open(TRAIN_PY) as f:
        orig = f.read()
    with open(BACKUP, "w") as f:
        f.write(orig)
    print(f"Backed up {TRAIN_PY} -> {BACKUP}")
else:
    with open(BACKUP) as f:
        orig = f.read()
    print(f"Restoring from {BACKUP}")

src = orig

# --- Edit 1: Add config variables after the existing config section ---
# Find the last config variable before exec(configurator)
config_marker = "exec(open('configurator.py').read())"
if config_marker not in src:
    # Try alternate form
    config_marker = 'exec(open("configurator.py").read())'
if config_marker not in src:
    print("WARNING: Could not find configurator.py exec line")

if config_marker in src:
    new_config_vars = """
# --- MUON PATCH: config variables ---
muon_optimizer = 'adamw'        # 'adamw' | 'muon'
muon_lr = 0.02
muon_momentum = 0.95
spectral_log_every = 500        # 0 = disabled
spectral_full_svd = True
seed = 1337
# --- END MUON PATCH ---
"""
    idx = src.index(config_marker)
    src = src[:idx] + new_config_vars + "\n" + src[idx:]
    print("Added config variables")

# --- Edit 2: Add import at top ---
import_line = "from muon_utils import MuonOptimizer, SpectralLogger, compute_spectral_metrics\n"
if import_line.strip() not in src:
    # Add after "import os"
    if "import os\n" in src:
        src = src.replace("import os\n", "import os\n" + import_line, 1)
    else:
        src = import_line + src
    print("Added muon_utils import")

# --- Edit 3: Replace optimizer creation ---
# Find: optimizer = model.configure_optimizers(...)
opt_pattern = re.compile(
    r'^(\s*)(optimizer\s*=\s*model\.configure_optimizers\(.+?\))',
    re.MULTILINE
)
match = opt_pattern.search(src)
if match:
    indent = match.group(1)
    replacement = f"""{indent}# --- MUON PATCH: optimizer selection ---
{indent}if muon_optimizer == 'muon':
{indent}    _muon_params = [p for p in model.parameters() if p.requires_grad]
{indent}    optimizer = MuonOptimizer(_muon_params, lr=muon_lr, momentum=muon_momentum, weight_decay=weight_decay)
{indent}    print(f"Using MuonOptimizer (lr={{muon_lr}}, momentum={{muon_momentum}})")
{indent}else:
{indent}    optimizer = model.configure_optimizers(weight_decay, learning_rate, (beta1, beta2), device_type)
{indent}# --- END MUON PATCH ---"""
    src = src[:match.start()] + replacement + src[match.end():]
    print("Replaced optimizer creation")
else:
    print("WARNING: Could not find model.configure_optimizers line")

# --- Edit 4: Add seed setting before training loop ---
loop_markers = ["for iter_num in range", "while True:"]
for lm in loop_markers:
    if lm in src:
        idx = src.index(lm)
        # Find start of line
        line_start = src.rfind('\n', 0, idx) + 1
        indent = src[line_start:idx].replace(src[line_start:idx].lstrip(), '')
        if not indent:
            indent = ''
        seed_block = f"""{indent}# --- MUON PATCH: seed + spectral logger ---
{indent}import torch as _torch_seed; _torch_seed.manual_seed(seed)
{indent}if _torch_seed.cuda.is_available(): _torch_seed.cuda.manual_seed(seed)
{indent}_spectral_logger = None
{indent}if spectral_log_every > 0:
{indent}    _spectral_logger = SpectralLogger(os.path.join(out_dir, 'spectral'), log_every=spectral_log_every, full_svd=spectral_full_svd)
{indent}# --- END MUON PATCH ---
"""
        src = src[:line_start] + seed_block + src[line_start:]
        print(f"Added seed + spectral logger init before '{lm}'")
        break

# --- Edit 5: Add spectral logging after optimizer.step() or scaler.update() ---
for step_marker in ['scaler.update()', 'optimizer.step()']:
    if step_marker in src:
        idx = src.index(step_marker) + len(step_marker)
        # Go to end of line
        eol = src.index('\n', idx)
        # Detect indent from the step_marker line
        line_start = src.rfind('\n', 0, idx) + 1
        raw_line = src[line_start:eol]
        indent = raw_line[:len(raw_line) - len(raw_line.lstrip())]
        log_snippet = f"""
{indent}# --- MUON PATCH: spectral log ---
{indent}if _spectral_logger is not None:
{indent}    _spectral_logger.maybe_log(model, iter_num, extra={{'train_loss': lossf if 'lossf' in dir() else None}})
{indent}# --- END MUON PATCH ---"""
        src = src[:eol] + log_snippet + src[eol:]
        print(f"Added spectral logging after {step_marker}")
        break

# --- Write patched file ---
with open(TRAIN_PY, 'w') as f:
    f.write(src)

# --- Verify no syntax errors ---
try:
    compile(src, TRAIN_PY, 'exec')
    print(f"\nPatch applied successfully. {TRAIN_PY} compiles without errors.")
except SyntaxError as e:
    print(f"\nWARNING: Patched file has syntax error at line {e.lineno}: {e.msg}")
    print("Restoring backup...")
    with open(BACKUP) as f:
        orig = f.read()
    with open(TRAIN_PY, 'w') as f:
        f.write(orig)
    print("Backup restored. Patch failed — manual intervention needed.")
    sys.exit(1)

print("\nNew config variables (use --muon_optimizer=muon on command line):")
print("  muon_optimizer = 'adamw' | 'muon'")
print("  muon_lr = 0.02")
